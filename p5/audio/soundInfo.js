class SoundInfo {
  constructor(genre, partName, bpm, timeSignatureUpper, timeSignatureLower, length) {
    this.genre = genre;
    this.partName = partName;
    this.bpm = bpm;
    this.timeSignatureUpper = timeSignatureUpper;
    this.timeSignatureLower = timeSignatureLower;
    this.length = length;
  }
}